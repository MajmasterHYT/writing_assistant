def generate_prompt(text):
    prompt = f'''
你是一名专业的文字编辑，请帮助我修改以下文学文本中的问题。文本中可能存在表达不清、修辞不当、情感色彩不明确、句式过于复杂或冗长、语法错误、风格不一致等问题。请逐句或逐段指出需要修改的部分，并给出修改建议。请注意，文本风格应保持文学性和情感的表达，但同时应确保语言清晰、简洁且富有表现力。

每项为一个对象，包含：

"原文"：原始文本中所有有问题的部分
"修改后"：你建议的修改版本

请注意： 
1. 对于每个问题，尽量保留原意并优化表达方式；
2. 如果某些句子冗长或结构复杂，请分割成更清晰易懂的句子；
3. 如遇情感表达不清或修辞有失当之处，请提出修改建议；
4. 保持文本的文学风格和情感色彩。

以下是一条示例：

json

[
  {{
    "原文": "他走进那座充满陈年尘土的小屋，仿佛时光倒流，回到了那些久远的岁月。",
    "修改后": "他走进那座尘土飞扬的小屋，仿佛回到了久远的过去，时光在这里停滞。"
  }}
]
请按照上述格式处理下方这段文字中的问题内容：
{text}
'''
    return prompt
# 示例：调用 generate_prompt 函数
prompt = generate_prompt("这个产品可能有点问题，用户体验不是很好。")
print(prompt)